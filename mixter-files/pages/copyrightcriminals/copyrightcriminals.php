<p >Drawing from more than fifty interviews with prominent musicians, artists, scholars, lawyers, and music industry representatives, Copyright Criminals looks at the development of sound collage (also known as sampling). The film explores the complicated impact that copyright law has had on the creative practice of sampling and examines the conflicting opinions artists and others have about appropriation.</p>
<p >"This contest, like our documentary, examines what it means to be creative in an age of digital reproduction," says Kembrew McLeod, co-director of Copyright Criminals. "Artists have traditionally borrowed from each other and have been directly inspired by the world around them. But what happens when digital technologies allow for very literal quotes to be inserted into new works?"</p>
