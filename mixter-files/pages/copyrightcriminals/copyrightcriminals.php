<h2  style="font-size:18px;margin:0px;font-weight:normal;">Judges</h2>
<p ><b ><a  href="http://www.cantstopwontstop.com/blog/">Jeff Chang</a></b> is the the author of the American Book Award-winning Can't 
Stop Won't Stop: A History of the Hip-Hop Generation. In 1993, he 
co-founded and ran the influential hip-hop indie label, SoleSides, now 
Quannum Projects, helping launch the careers of DJ Shadow, 
Blackalicious, Lyrics Born and Lateef the Truth Speaker. He has helped 
produce over a dozen records, including the "godfathers of gangsta 
rap", the Watts Prophets.</p>
<p ><b >Ben Franzen</b> is an Atlanta-based artist who owns an independent production 
company, Changing Images LLC, which specializes in video, photography, and 
multimedia. Franzen currently edits a broadcast program Squidbillies, which 
appears on the Cartoon Network.</p>
<p ><b ><a  href="http://www.kembrew.com/">Kembrew McLeod</a></b> is both a professor at the University of Iowa and an 
award-winning independent documentary filmmaker. McLeod has written music 
criticism for Rolling Stone, the Village Voice and MOJO, and has authored two 
books, most recently Freedom of Expression&reg;: Overzealous Copyright Bozos 
and Other Enemies of Creativity (Doubleday).</p>
