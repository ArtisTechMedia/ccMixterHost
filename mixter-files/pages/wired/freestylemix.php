<h2  style="font-size:18px;margin:0px;font-weight:normal;">The Militia Mix Contest</h2>
<p >Entrants in the Militia Mix used track 11 from the Wired CD, "No Meaning No," by Chuck D and <a  href="http://www.fineartsmilitia.com/">the Fine Arts Militia</a>, took samples from it, and combined them with their own creativity.  The one best entry will be on the next Fine Arts Militia album, to be released in the spring. </p>
