<h2  style="font-size:18px;margin:0px;font-weight:normal;">The Freestyle Mix Contest</h2>
<p >Entrants in the Freestyle mix selected samples from any of the remaining fifteen tracks on the WIRED CD. The top 11 entries in the Freestyle Mix will go on the Creative Commons release THE WIRED CD: Ripped. Sampled. Mashed. Shared.  to be promoted at the M3 Music Summit, South by Southwest music festival, the World Social Forum, Ready to Share, and the <a  href="http://creativecommons.org/">Creative Commons web site</a>, among other places. </p>
