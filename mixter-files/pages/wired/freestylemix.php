<h2  style="font-size:18px;margin:0px;font-weight:normal;">What it was:</h2>
<p >A contest to sample <a  href="http://creativecommons.org/">Creative Commons</a> licensed tracks from <a  href="http://creativecommons.org/wired/">THE WIRED CD -- Rip. Mix. Sample. Mash. Share.</a>
</p>
<p >The Fine Art of Sampling Contest built upon the CD, featuring tracks licensed under <a  href="http://creativecommons.org/projects/sampling">the Sampling licenses</a>, giving you the right to rip, sample, mash, and share, without legal hassle. </p>
<p >There were two entry categories:</p>
